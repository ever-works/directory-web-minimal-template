/** Input shape for {@link generateLlmsFullTxt}. */
export interface LlmsFullTxtInput {
    /** Site name (rendered as the H1). */
    siteName: string;
    /** Absolute base URL of the site. */
    siteUrl: string;
    /** Optional one-line description rendered as a blockquote under the H1. */
    description?: string;
    /** All categories (rendered as a bulleted list under "## Categories"). */
    categories?: ReadonlyArray<{ id?: string; name?: string; count?: number }>;
    /** All tags (rendered as a bulleted list under "## Tags"). */
    tags?: ReadonlyArray<{ id?: string; name?: string; count?: number }>;
    /** All items (rendered with their full body content under "## Items"). */
    items?: ReadonlyArray<MirrorItem>;
    /** All comparisons (rendered under "## Comparisons"). */
    comparisons?: ReadonlyArray<MirrorComparison>;
    /**
     * Cap on the number of items rendered with full bodies. Defaults
     * to 500. Sites with thousands of items can exceed reasonable
     * response sizes; agents that need every item should fall back to
     * /items.json or per-page .md mirrors.
     */
    maxItemsWithBody?: number;
}

/**
 * Render the entire directory as a single Markdown document suitable
 * for serving at `/llms-full.txt`.
 *
 * Format mirrors the llmstxt.org convention:
 *
 *   # Site
 *   > Description
 *   ## Categories
 *   ## Tags
 *   ## Items
 *   ### Item 1
 *   ### Item 2
 *   ## Comparisons
 */
export function generateLlmsFullTxt(input: LlmsFullTxtInput): string {
    const cap = input.maxItemsWithBody ?? 500;
    const baseUrl = trimBase(input.siteUrl);

    const lines: string[] = [];
    lines.push(`# ${input.siteName}`);
    lines.push('');
    if (input.description) {
        lines.push(`> ${input.description}`);
        lines.push('');
    }
    lines.push(`Generated: ${new Date().toISOString()}`);
    lines.push(`Items: ${input.items?.length ?? 0}`);
    if (baseUrl) lines.push(`Canonical site: ${baseUrl}`);
    lines.push('');

    if (input.categories && input.categories.length > 0) {
        lines.push('## Categories');
        lines.push('');
        for (const c of input.categories) {
            const name = c.name ?? c.id ?? '';
            if (!name) continue;
            lines.push(`- ${name}${c.count ? ` — ${c.count} items` : ''}`);
        }
        lines.push('');
    }

    if (input.tags && input.tags.length > 0) {
        lines.push('## Tags');
        lines.push('');
        const top = input.tags.slice(0, 100);
        for (const t of top) {
            const name = t.name ?? t.id ?? '';
            if (!name) continue;
            lines.push(`- ${name}${t.count ? ` — ${t.count} items` : ''}`);
        }
        if (input.tags.length > top.length) {
            lines.push('');
            lines.push(`_…and ${input.tags.length - top.length} more (see ${baseUrl}/tags)._`);
        }
        lines.push('');
    }

    if (input.items && input.items.length > 0) {
        lines.push('## Items');
        lines.push('');
        const cappedItems = input.items.slice(0, cap);
        for (const item of cappedItems) {
            const md = renderItemMarkdown(item, { baseUrl });
            lines.push(demoteAndStrip(md));
            lines.push('');
            lines.push('---');
            lines.push('');
        }

        if (input.items.length > cappedItems.length) {
            lines.push(
                `_${input.items.length - cappedItems.length} additional items omitted to keep this file usable. See ${baseUrl}/items.json or per-item .md mirrors at ${baseUrl}/item/<slug>.md._`
            );
            lines.push('');
        }
    }

    if (input.comparisons && input.comparisons.length > 0) {
        lines.push('## Comparisons');
        lines.push('');
        for (const cmp of input.comparisons) {
            const md = renderComparisonMarkdown(cmp, { baseUrl });
            lines.push(demoteAndStrip(md));
            lines.push('');
            lines.push('---');
            lines.push('');
        }
    }

    return lines.join('\n');
}

/**
 * Demote heading levels by one and strip the trailing
 * "_This Markdown mirror is generated by…_" / "_Canonical page:_" footer
 * from a per-page mirror so it composes cleanly inside llms-full.txt.
 */
function demoteAndStrip(md: string): string {
    return md
        .split('\n')
        .filter(
            (line) =>
                !/^_This Markdown mirror is generated by Ever Works/.test(line) &&
                !/^_Canonical page:/.test(line)
        )
        .join('\n')
        .replace(/^(#{1,5}) /gm, '$1# ')
        .replace(/\n{3,}/g, '\n\n')
        .trim();
}
