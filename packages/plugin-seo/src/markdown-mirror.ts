/**
 * Per-page Markdown mirrors and the long-form llms-full.txt dump.
 *
 * These helpers are pure: each takes a normalized data object and
 * returns a Markdown string. No I/O. They power the Astro template's
 * `.md` mirror endpoints (one per item / category / tag / collection /
 * comparison / static page) and the concatenated `/llms-full.txt`
 * resource that ChatGPT, Claude, Perplexity, and other AI agents can
 * consume in a single fetch.
 *
 * Why expose Markdown twins?
 *
 * - HTML is laden with chrome (nav, footers, scripts, ads) that wastes
 *   an agent's context budget and confuses extraction.
 * - The `.md` mirror gives agents the page's substantive content with
 *   zero noise, taken straight from the YAML/Markdown source files.
 * - The HTML page advertises its mirror via
 *   `<link rel="alternate" type="text/markdown" href="/items/foo.md" />`.
 *
 * @see https://llmstxt.org/
 */

// Note: types are duplicated here as structural shapes rather than imported
// from `@ever-works/core` so this module stays a leaf dependency of plugin-seo.
// The shapes match `ItemData`, `Category`, `Tag`, `CollectionData`, and
// `ComparisonData` exactly.

/** Minimal item shape consumed by {@link renderItemMarkdown}. */
export interface MirrorItem {
    name: string;
    slug: string;
    description?: string;
    source_url?: string;
    category?: string | string[];
    tags?: string[];
    featured?: boolean;
    icon_url?: string;
    publisher?: string;
    updated_at?: string;
    markdown?: string;
}

/** Minimal category shape consumed by {@link renderCategoryMarkdown}. */
export interface MirrorCategory {
    id: string;
    name: string;
    description?: string;
}

/** Minimal tag shape consumed by {@link renderTagMarkdown}. */
export interface MirrorTag {
    id: string;
    name: string;
}

/** Minimal collection shape consumed by {@link renderCollectionMarkdown}. */
export interface MirrorCollection {
    id: string;
    slug: string;
    name: string;
    description?: string;
    icon_url?: string;
    items?: string[];
}

/** Minimal comparison shape consumed by {@link renderComparisonMarkdown}. */
export interface MirrorComparison {
    slug: string;
    title: string;
    summary?: string;
    item_a_name: string;
    item_b_name: string;
    item_a_slug: string;
    item_b_slug: string;
    dimensions?: ReadonlyArray<{ name?: string; title?: string }>;
    generated_at?: string;
}

/** Options shared by every renderer. */
export interface MarkdownMirrorOptions {
    /** Absolute base URL of the site (e.g. `'https://example.com'`). */
    baseUrl: string;
}

/** Strip a trailing slash from the base URL so we can `${base}/path`. */
function trimBase(base: string): string {
    return base.replace(/\/+$/, '');
}

function categoryLabel(cat: MirrorItem['category']): string {
    if (!cat) return '';
    return Array.isArray(cat) ? cat.filter(Boolean).join(', ') : cat;
}

/** Render a single directory item to Markdown. */
export function renderItemMarkdown(item: MirrorItem, options: MarkdownMirrorOptions): string {
    const base = trimBase(options.baseUrl);
    const canonicalUrl = `${base}/item/${item.slug}`;

    const lines: string[] = [];
    lines.push(`# ${item.name}`);
    lines.push('');
    if (item.description) {
        lines.push(`> ${item.description.replace(/\n+/g, ' ')}`);
        lines.push('');
    }

    const facts: string[] = [];
    if (item.source_url) facts.push(`- **URL:** ${item.source_url}`);
    const cat = categoryLabel(item.category);
    if (cat) facts.push(`- **Category:** ${cat}`);
    if (item.tags && item.tags.length > 0) facts.push(`- **Tags:** ${item.tags.join(', ')}`);
    if (item.featured) facts.push(`- **Featured:** yes`);
    if (item.publisher) facts.push(`- **Publisher:** ${item.publisher}`);
    if (item.updated_at) facts.push(`- **Updated:** ${item.updated_at}`);
    facts.push(`- **Canonical page:** ${canonicalUrl}`);
    lines.push(...facts);
    lines.push('');

    const body = (item.markdown ?? '').trim();
    if (body) {
        lines.push('## Details');
        lines.push('');
        lines.push(body);
        lines.push('');
    }

    lines.push('---');
    lines.push('');
    lines.push(`_This Markdown mirror is generated by Ever Works for AI agents. The canonical HTML page is at ${canonicalUrl}._`);
    lines.push('');

    return lines.join('\n');
}

/** Render a category page (items in the category) to Markdown. */
export function renderCategoryMarkdown(
    category: MirrorCategory | undefined,
    items: MirrorItem[],
    options: MarkdownMirrorOptions & { categoryRef: string }
): string {
    const base = trimBase(options.baseUrl);
    const canonicalUrl = `${base}/category/${options.categoryRef}`;
    const name = category?.name ?? options.categoryRef;

    const lines: string[] = [];
    lines.push(`# ${name}`);
    lines.push('');
    lines.push(`> Browse ${items.length} ${items.length === 1 ? 'item' : 'items'} in ${name}.`);
    lines.push('');
    if (category?.description) {
        lines.push(category.description);
        lines.push('');
    }

    if (items.length > 0) {
        lines.push('## Items');
        lines.push('');
        for (const item of items) {
            const url = `${base}/item/${item.slug}`;
            const desc = (item.description ?? '').replace(/\n+/g, ' ');
            lines.push(`- [${item.name}](${url})${desc ? ` — ${desc}` : ''}`);
        }
        lines.push('');
    }

    lines.push('---');
    lines.push('');
    lines.push(`_Canonical page: ${canonicalUrl}_`);
    lines.push('');
    return lines.join('\n');
}

